public class Nodo {
    private String elem;
    private Nodo next;

    public String getElem() {
        return elem;
    }
    public void setElem(String variable) {
        elem = variable;
    }
    public Nodo getNext() {
        return next;
    }
    public void setNext(Nodo variable) {
        next = variable;
    }
}
