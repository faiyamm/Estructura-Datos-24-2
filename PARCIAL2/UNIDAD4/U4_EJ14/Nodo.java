public class Nodo {
    private int Elem;
    private Nodo Right;
    private Nodo Left;
    
    public int getElem() {
        return Elem;
    }
    public void setElem(int variable) {
        Elem = variable;
    }
    public Nodo getRight() {
        return Right;
    }
    public void setRight(Nodo right) {
        Right = right;
    }
    public Nodo getLeft() {
        return Left;
    }
    public void setLeft(Nodo left) {
        Left = left;
    } 
}